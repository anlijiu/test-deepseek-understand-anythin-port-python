""".understandignore 文件生成器。

提供两种生成模式：

1. **主动生效模式** — :func:`generate_understandignore` 根据传入的语言/框架参数
   生成直接生效的 ``.understandignore`` 文件（Python 便利 API）。

2. **Starter 建议模式** — :func:`generate_starter_ignore_file` 扫描项目目录和
   ``.gitignore``，生成注释形式的建议文件（对标 TypeScript
   ``generateStarterIgnoreFile`` 的等价实现），由用户手动取消注释来启用。
"""

